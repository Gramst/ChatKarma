def command_ach(karma_val, karma_ach_now):
    add_res = [i for i in ACH if i['start'] <= karma_val < i['stop']]
    del_res = [i for i in ACH if i['em_ach'] == karma_ach_now and karma_val > i['stop']]
    if add_res:
        if add_res[0].get('em_ach') == karma_ach_now:
            add_res = []
    return add_res, del_res

